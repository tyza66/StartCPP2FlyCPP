#include <iostream>
using namespace std;

int main14() {
	//引用必须在声明的时候初始化
	 // 声明简单的变量
	int    i;
	double d;

	// 声明引用变量
	int& r = i;
	double& s = d;

	i = 5;
	cout << "Value of i : " << i << endl;
	cout << "Value of i reference : " << r << endl;

	d = 11.7;
	cout << "Value of d : " << d << endl;
	cout << "Value of d reference : " << s << endl;

	//差别就是在创建的时候不开辟空间，而是使用原来的那块空间 相当于变量的一个别名
	return 0;
}